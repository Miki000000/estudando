// OPERADORES DE ATRIBUIÇÃO
// O primeiro operador de atribuição é o "=", utilizado ao atribuir valores à uma variavel, como:
let n3 = 10; // <- Atribuição
console.log(n3);
// Quando é uma váriavel númerica, também é possivel atribuir um operador aritmédico dentro do operador de atribuição diretamente.
// No exemplo abaixo, o valor será resultante como 0, pois foi subtraído do valor total da variavel, e isso funciona para todos, como multiplicação, divisão, etc.
n3 -= 10;
console.log(n3);
n3 = 10;
n3 *= 20;
console.log(n3);
