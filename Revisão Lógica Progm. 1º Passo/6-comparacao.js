// COMPARÇÃO
// Existem vários tipos de operadores de comparação, os mais conhecido sendo:
// Compara se os valores são iguais, ou seja, se forem, retornará true: ==
// Compara se os valores são DIFERENTES, ou seja, se forem diferentes, retornará true, se forem iguais, false: !=
// Compara se os valores e o tipo são iguais ou seja, se forem, retornará true: ===
// Compara se os valores e o tipo são DIFERENTES ou seja, se forem diferentes, retornará true, se forem iguais, false: !==
// Compara se um valor é maior, menor, maior ou igual, ou menor ou igual, a outro valor: <, >, <=,  =>
// Exemplos:
let i = 0;
console.log(i == '0'); // <- Valor igual, mas tipo diferente
console.log(i != 1); // <- Valor diferente, mesmo sendo mesmo tipo
console.log(i === 0); // <- Valor igual e tipos iguais
console.log(i !== '1'); // <- Valor e tipo diferentes.
console.log(i < 1); // <- Valor menor que um.
console.log(i + 2 > 1); // <- Valor maior que um
console.log(i <= 1); // <- Valor menor ou igual a um.
console.log(i + 1 >= 1); // <- Valor maior ou igual a um.
