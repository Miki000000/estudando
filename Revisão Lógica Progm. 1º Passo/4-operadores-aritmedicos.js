// OPERADORES ARITMÉDICOS:
// Em Js, existem 6 tipos de operadores, sendo eles:
// + (soma)
// - (subtração)
// * (multiplicação)
// / (divisão)
// % (módulo)
// ** (potencialização)
// Exemplos:
let n1 = 2;
let n2 = 10;
console.log(`2 mais 10 é ${n1 + n2}`); //<- soma
console.log(`2 menos 10 é ${n1 - n2}`); //<- subtração
console.log(`2 vezes 10 é ${n1 * n2}`); //<- multiplicação
console.log(`2 dividido por 10 é ${n1 / n2}`); //<- divisão
console.log(`módulo de 2 em 10 é ${n1 % n2}`); //<- modulo
console.log(`2 elevado à 10 é ${n1 ** n2}`); //<- potencialização
