// DEFINIR VARIAVEIS:
// let _ = _ ; Define uma váriavel que pode ser mutável. Exemplo:
let ola = 'ola';
// essa váriavel pode ser mudada ao chamála e definir novamente. Exemplo:
ola = 3;
console.log(ola);
// const = _ ; Define uma váriavel constante que não pode ser mudada.
const ola2 = 'ola';
// essa váriavel não pode ser mudada caso eu chame-a novamente e tente alterar seu valor. Exemplo:
// ola2 = 2;
console.log(ola2);
